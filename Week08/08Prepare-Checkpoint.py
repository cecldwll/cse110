# Understanding 'for' Loops

# 1. Iterate through a list of items
colors = ['red', 'blue', 'green', 'yellow']

print()
for color in colors:
    print(color)
print()

# 2. Iterate through a list of numbers
for num in range(1, 9):
    print(num)
print()

# 3. Iterate through even numbers
for num in range(2, 21, 2):
    print(num)
print()